package code1_4;

public class Main {

	public static void main(String[] args) {
			double pi = 3.14; //円周率
			int pie = 5; //半径
			System.out.println("半径" + pie + "㎝のパイの面積は、");
			System.out.println(pie * pie * pi);
			System.out.println("パイの半径を倍にします");
			pi = 10; //円周率に代入
			System.out.println("半径" + pie + "cmのパイの半径は、");
			System.out.println(pie * pie * pi);

	}

}
